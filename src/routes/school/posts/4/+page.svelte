<h1>Филиал МЭСИ</h1>
<p>Набирает абитуриентов на программы среднего и высшего образования, а также повышения квалификации.</p>
<p>Университет носит государственный статус.</p>
<h3>Программы подготовки:</h3>
<ul>
    <li>Право и организация социального обеспечения.</li>
    <li>Отраслевая экономика и бухгалтерский учет.</li>
</ul>
<p>Филиал не имеет общежития и военной кафедры.</p>
<p>Адрес Хакасского филиала МЭСИ: улица Тараса Шевченко, 84А.</p>

